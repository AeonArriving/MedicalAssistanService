USE medical_assistant_db;

CREATE TABLE
IF NOT EXISTS Disease_Details (
	KeyId CHAR (16) NOT NULL DEFAULT '' COMMENT '疾病id',
	DiseaseName VARCHAR (32) NULL DEFAULT '' COMMENT '疾病名',
	PainPerception VARCHAR (64) NULL DEFAULT '' COMMENT '疼痛的感觉',
	PainRegion VARCHAR (64) NULL DEFAULT '' COMMENT '疼痛的部位',
	PainDuration VARCHAR (64) NULL DEFAULT '' COMMENT '疼痛的持续时间',
	SymptomWorsen VARCHAR (255) NULL DEFAULT '' COMMENT '病症在什么情况下会恶化',
	OtherFeaturesOfPain VARCHAR (255) NULL DEFAULT '' COMMENT '疼痛的其它特征',
	SymptomReason VARCHAR (255) NULL DEFAULT '' COMMENT '何种因素可引发此症状',
	SymptomRelieved VARCHAR (255) NULL DEFAULT '' COMMENT '何种做法可减轻症状',
	SymptomStart VARCHAR (255) NULL DEFAULT '' COMMENT '症状何时开始',
	SymptomWith VARCHAR (255) NULL DEFAULT '' COMMENT '伴有',
	SymptomFelling VARCHAR (255) NULL DEFAULT '' COMMENT '你会感觉到(头晕特有属性)',
	BloodPosition VARCHAR (255) NULL DEFAULT '' COMMENT '血出现在(大便带血特有属性)',
	OtherFeatures VARCHAR (255) NULL DEFAULT '' COMMENT '症状的其它特征(视力问题特有属性)',
	Coughing VARCHAR (255) NULL DEFAULT '' COMMENT '咳嗽表现为(咳嗽特有属性)',
	AffectedArea VARCHAR (255) NULL DEFAULT '' COMMENT '受影响或累及部位为(足部或踝关节疼痛特有属性)',
	SymptomAppears VARCHAR (255) NULL DEFAULT '' COMMENT '病症出现在下面哪种情况之后(吞咽特有属性)',
	SwallowFelling VARCHAR (255) NULL DEFAULT '' COMMENT '吞咽时(吞咽特有属性)',
	Summary text COMMENT '概述',
	SymptomDetails text COMMENT '症状',
	CauseOfDisease text COMMENT '病因',
	RiskFactor text COMMENT '风险因素',
	Diagnosis text COMMENT '诊断',
	Complications text COMMENT '并发症',
	Treatment text COMMENT '治疗',
	Prevention text COMMENT '预防',
	SelfCare text COMMENT '自我护理',
	UnCommonTreat text COMMENT '非常规疗法',
	SeeDoctorTime text COMMENT '何时求医',
	SymptomAndComplications text COMMENT '症状和并发症(假性痛风)',
	TreatmentAndPrevention text COMMENT '治疗和预防(假性痛风)',
	ScreenAndDiagnosis text COMMENT '筛查和诊断(肝硬化)',
	ReplacementTherapy text COMMENT '补充疗法(肝硬化)',
	DiagnosisAndTreatment text COMMENT '诊断和治疗',
	DiabetesTypes text COMMENT '糖尿病类型',
	DiabetesDiagnosis text COMMENT '糖尿病诊断',
	DiabetesPrediabetes text COMMENT '前驱糖尿病',
	DiabetesTypeI text COMMENT 'Ⅰ型糖尿病',
	DiabetesTypeII text COMMENT 'Ⅱ型糖尿病',
	GDM text COMMENT '妊娠糖尿病',
	DiabetesFootCare text COMMENT '糖尿病足部护理',
	NutritionAdvice text COMMENT '营养和饮食建议',
	UrlDisease VARCHAR (64) NULL DEFAULT '' COMMENT '疾病Url',
	MatchingTimes INT NULL DEFAULT 0 COMMENT '匹配热度',
	ModifyTime datetime NOT NULL DEFAULT '1900-01-01 00:00:00' COMMENT '修改时间',
	PRIMARY KEY (KeyId),
	INDEX IDX_Disease_Details_○ (SymptomWith)
)