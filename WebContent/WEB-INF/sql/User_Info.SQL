USE Medical_Assistant_DB;

CREATE TABLE
IF NOT EXISTS User_Info (
	UserName VARCHAR (16) NOT NULL DEFAULT '' COMMENT '用户名(5到15位)',
	PASSWORD VARCHAR (64) NOT NULL DEFAULT '' COMMENT '密码(5到15位)',
	UserType INT NOT NULL DEFAULT 0 COMMENT '用户类型',
	TelePhone VARCHAR (11) NULL DEFAULT '' COMMENT '移动电话',
	FixedPhone VARCHAR (16) NULL DEFAULT '' COMMENT '固话',
	Address VARCHAR (255) NULL DEFAULT '' COMMENT '家庭地址',
	Postcode VARCHAR (8) NULL DEFAULT '' COMMENT '邮编',
	email VARCHAR (32) NULL DEFAULT '' COMMENT '邮箱',
	Birthday datetime NULL DEFAULT '1900-01-01 00:00:00' COMMENT '生日',
	DocumenTType VARCHAR (255) NULL DEFAULT '' COMMENT '证件类型',
	DocumentID VARCHAR (255) NULL DEFAULT '' COMMENT '证件号',
	ModifyTime datetime NOT NULL DEFAULT now() COMMENT '修改时间',
	PRIMARY KEY (UserName)
)